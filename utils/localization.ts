export const getTranslation = (key: keyof typeof EN_UI, lang: Language): string => {
  const langPack = TRANSLATIONS[lang] || TRANSLATIONS['en'];
  return langPack[key] || EN_UI[key] || key;
};

export const getBrowserLang = (): Language => {
  const browserLang = navigator.language.split('-')[0];
  const found = SUPPORTED_LANGUAGES.find(l => l.code === browserLang);
  return found ? (found.code as Language) : 'en';
};
